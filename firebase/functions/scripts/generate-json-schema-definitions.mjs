// Parses the GraphQL SDL (generated/Graphql) and emits JSON Schema definitions
// for every input type whose name ends in "Fields". These definitions power the
// backend-driven form builder (src/Utils/SchemaForms.ts): the backend composes
// {jsonSchema, uiSchema, defaultData} responses so clients render entity
// create/edit forms with zero frontend form code.
//
// Output: generated/GraphqlSchemaDefinitions.ts with
//   - graphqlSchemaDefinitions: Record<key, JsonSchemaDefinition>
//   - GraphqlSchemaDefinitionKey: union of all definition keys
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"
import {
    buildSchema,
    isEnumType,
    isInputObjectType,
    isNonNullType,
    isScalarType,
    getNamedType,
} from "graphql"

const functionsDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const graphqlDir = path.join(functionsDir, "generated", "Graphql")

function collectSdlFiles(dir) {
    const entries = fs.readdirSync(dir, { withFileTypes: true })
    return entries.flatMap((entry) => {
        const fullPath = path.join(dir, entry.name)
        if (entry.isDirectory()) return collectSdlFiles(fullPath)
        return entry.name.endsWith(".gql") ? [fullPath] : []
    })
}

const sdl = collectSdlFiles(graphqlDir)
    .map((file) => fs.readFileSync(file, "utf8"))
    .join("\n")
const schema = buildSchema(sdl)

// Scalars that JSON Schema represents as plain strings.
const stringScalars = new Set(["String", "Id", "ID", "Instant"])

function jsonSchemaPropertyForType(namedType) {
    if (isScalarType(namedType)) {
        if (stringScalars.has(namedType.name)) return { type: "string" }
        if (namedType.name === "Int") return { type: "integer" }
        if (namedType.name === "Float") return { type: "number" }
        if (namedType.name === "Boolean") return { type: "boolean" }
        return { type: "string" }
    }
    if (isEnumType(namedType)) {
        return { type: "string", enum: namedType.getValues().map((value) => value.name) }
    }
    throw new Error(
        `Unsupported field type "${namedType.name}" in a *Fields input type. ` +
            "The starter form builder supports scalars and enums only.",
    )
}

const definitions = {}
for (const [typeName, type] of Object.entries(schema.getTypeMap())) {
    if (!isInputObjectType(type) || !typeName.endsWith("Fields")) continue

    const properties = {}
    const required = []
    for (const field of Object.values(type.getFields())) {
        properties[field.name] = jsonSchemaPropertyForType(getNamedType(field.type))
        if (isNonNullType(field.type)) required.push(field.name)
    }
    definitions[typeName] = { properties, required }
}

const keys = Object.keys(definitions).sort()
if (keys.length === 0) {
    throw new Error("No *Fields input types found in the GraphQL schema.")
}

const keyUnion = keys.map((key) => JSON.stringify(key)).join(" | ")
const output = `// Generated by scripts/generate-json-schema-definitions.mjs. Do not edit.

export interface JsonSchemaProperty {
    type: "string" | "integer" | "number" | "boolean"
    enum?: string[]
}

export interface JsonSchemaDefinition {
    properties: Record<string, JsonSchemaProperty>
    required: string[]
}

export type GraphqlSchemaDefinitionKey = ${keyUnion}

export const graphqlSchemaDefinitions: Record<GraphqlSchemaDefinitionKey, JsonSchemaDefinition> = ${JSON.stringify(
    Object.fromEntries(keys.map((key) => [key, definitions[key]])),
    undefined,
    4,
)}
`

fs.writeFileSync(path.join(functionsDir, "generated", "GraphqlSchemaDefinitions.ts"), output)
console.log(`generate-json-schema-definitions: wrote ${keys.length} definitions (${keys.join(", ")}).`)
