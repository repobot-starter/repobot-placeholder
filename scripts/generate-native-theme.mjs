// Generates the native theme constants from the root repobot.theme.json so
// iOS and Android render the same brand the web design system derives at
// build time (web: web/design-system/src/theme/themeConfig.ts — the color
// math here mirrors it and must stay in sync).
//
// Emits:
//   ios/App/View/Theme/GeneratedTheme.swift
//   android/app/src/main/kotlin/com/baseapp/android/view/theme/GeneratedTheme.kt
//
// Run via `npm run codegen` (scripts/codegen.sh); generated defaults are
// committed so fresh clones build without codegen.

import { readFileSync, writeFileSync } from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")

/* --------------------------- color math (mirrors themeConfig.ts) -------- */

const HEX_PATTERN = /^#(?:[0-9a-fA-F]{3}|[0-9a-fA-F]{6})$/

function isHexColor(value) {
    return typeof value === "string" && HEX_PATTERN.test(value)
}

function hexToRgb(hex) {
    const normalized = hex.length === 4 ? `#${hex[1]}${hex[1]}${hex[2]}${hex[2]}${hex[3]}${hex[3]}` : hex
    return [
        parseInt(normalized.slice(1, 3), 16),
        parseInt(normalized.slice(3, 5), 16),
        parseInt(normalized.slice(5, 7), 16),
    ]
}

function rgbToHex(rgb) {
    return `#${rgb
        .map((channel) =>
            Math.round(Math.min(255, Math.max(0, channel)))
                .toString(16)
                .padStart(2, "0"),
        )
        .join("")}`
}

function mixHex(color, target, amount) {
    const from = hexToRgb(color)
    const to = hexToRgb(target)
    return rgbToHex([
        from[0] + (to[0] - from[0]) * amount,
        from[1] + (to[1] - from[1]) * amount,
        from[2] + (to[2] - from[2]) * amount,
    ])
}

function relativeLuminance(hex) {
    const [r, g, b] = hexToRgb(hex).map((channel) => {
        const scaled = channel / 255
        return scaled <= 0.03928 ? scaled / 12.92 : ((scaled + 0.055) / 1.055) ** 2.4
    })
    return 0.2126 * r + 0.7152 * g + 0.0722 * b
}

function contrastText(background) {
    return relativeLuminance(background) > 0.45 ? "#071223" : "#ffffff"
}

/* --------------------------- contract resolution ------------------------ */

const RADIUS_PRESETS = {
    sharp: { sm: 2, md: 4, lg: 6, pill: 999 },
    soft: { sm: 6, md: 10, lg: 14, pill: 999 },
    round: { sm: 10, md: 16, lg: 22, pill: 999 },
}

const DENSITY_PRESETS = {
    compact: { xxs: 2, xs: 3, sm: 6, md: 10, lg: 14, xl: 20 },
    comfortable: { xxs: 2, xs: 4, sm: 8, md: 12, lg: 16, xl: 24 },
    spacious: { xxs: 3, xs: 6, sm: 10, md: 16, lg: 22, xl: 32 },
}

const KERNEL_DEFAULT_PRIMARY = "#1f6feb"
const KERNEL_DEFAULT_PRIMARY_DARK = "#90caf9"

const config = JSON.parse(readFileSync(path.join(repoRoot, "repobot.theme.json"), "utf8"))

const primary = isHexColor(config.brand?.primary) ? config.brand.primary : KERNEL_DEFAULT_PRIMARY
const primaryDark = isHexColor(config.brand?.primaryDark)
    ? config.brand.primaryDark
    : primary === KERNEL_DEFAULT_PRIMARY
      ? KERNEL_DEFAULT_PRIMARY_DARK
      : mixHex(primary, "#ffffff", 0.4)

const radius = RADIUS_PRESETS[config.radius] ?? RADIUS_PRESETS.soft
const spacing = DENSITY_PRESETS[config.density] ?? DENSITY_PRESETS.comfortable
const mode = ["light", "dark", "system"].includes(config.mode) ? config.mode : "light"

// Presets native can honor (web additionally accepts raw CSS stacks, which
// have no native meaning — those fall back to the system font here). The
// web-font presets map to TTFs bundled in ios/App/Fonts and android res/font.
const FONT_PRESET_KEYS = [
    "system",
    "serif",
    "rounded",
    "mono",
    "inter",
    "manrope",
    "source-serif",
    "space-grotesk",
    "plex-mono",
]
const fontFamily = FONT_PRESET_KEYS.includes(config.fontFamily) ? config.fontFamily : "system"

const theme = {
    accentLight: primary,
    accentTextLight: contrastText(primary),
    accentDark: primaryDark,
    accentTextDark: contrastText(primaryDark),
    radius,
    spacing,
    defaultMode: mode,
    fontFamily,
}

/* --------------------------- emitters ----------------------------------- */

const header = (comment) =>
    `${comment} Generated by scripts/generate-native-theme.mjs from repobot.theme.json.\n` +
    `${comment} Do not edit — change repobot.theme.json and run \`npm run codegen\`.\n`

function swiftSource() {
    return (
        header("//") +
        `
import CoreGraphics

enum GeneratedTheme {
  // Brand accents (light / dark surfaces), derived like the web tokens.
  static let accentLight = "${theme.accentLight}"
  static let accentTextLight = "${theme.accentTextLight}"
  static let accentDark = "${theme.accentDark}"
  static let accentTextDark = "${theme.accentTextDark}"

  // Corner radius scale ("${config.radius ?? "soft"}").
  static let radiusSm: CGFloat = ${theme.radius.sm}
  static let radiusMd: CGFloat = ${theme.radius.md}
  static let radiusLg: CGFloat = ${theme.radius.lg}
  static let radiusPill: CGFloat = ${theme.radius.pill}

  // Spacing scale ("${config.density ?? "comfortable"}").
  static let spaceXxs: CGFloat = ${theme.spacing.xxs}
  static let spaceXs: CGFloat = ${theme.spacing.xs}
  static let spaceSm: CGFloat = ${theme.spacing.sm}
  static let spaceMd: CGFloat = ${theme.spacing.md}
  static let spaceLg: CGFloat = ${theme.spacing.lg}
  static let spaceXl: CGFloat = ${theme.spacing.xl}

  // Default UI mode from the contract ("system" follows the OS appearance).
  static let defaultMode = "${theme.defaultMode}"

  // Font preset from the contract; resolved in View/Theme/Theme.swift.
  static let fontFamily = "${theme.fontFamily}"
}
`
    )
}

function kotlinColor(hex) {
    return `0xFF${hex.slice(1).toUpperCase()}`
}

function kotlinSource() {
    return (
        header("//") +
        `
package com.baseapp.android.view.theme

object GeneratedTheme {
    // Brand accents (light / dark surfaces), derived like the web tokens.
    const val ACCENT_LIGHT = ${kotlinColor(theme.accentLight)}
    const val ACCENT_TEXT_LIGHT = ${kotlinColor(theme.accentTextLight)}
    const val ACCENT_DARK = ${kotlinColor(theme.accentDark)}
    const val ACCENT_TEXT_DARK = ${kotlinColor(theme.accentTextDark)}

    // Corner radius scale ("${config.radius ?? "soft"}"), in dp.
    const val RADIUS_SM = ${theme.radius.sm}
    const val RADIUS_MD = ${theme.radius.md}
    const val RADIUS_LG = ${theme.radius.lg}
    const val RADIUS_PILL = ${theme.radius.pill}

    // Spacing scale ("${config.density ?? "comfortable"}"), in dp.
    const val SPACE_XXS = ${theme.spacing.xxs}
    const val SPACE_XS = ${theme.spacing.xs}
    const val SPACE_SM = ${theme.spacing.sm}
    const val SPACE_MD = ${theme.spacing.md}
    const val SPACE_LG = ${theme.spacing.lg}
    const val SPACE_XL = ${theme.spacing.xl}

    // Default UI mode from the contract ("system" follows the OS appearance).
    const val DEFAULT_MODE = "${theme.defaultMode}"

    // Font preset from the contract; resolved in view/theme/Theme.kt.
    const val FONT_FAMILY = "${theme.fontFamily}"
}
`
    )
}

const swiftPath = path.join(repoRoot, "ios", "App", "View", "Theme", "GeneratedTheme.swift")
const kotlinPath = path.join(
    repoRoot,
    "android",
    "app",
    "src",
    "main",
    "kotlin",
    "com",
    "baseapp",
    "android",
    "view",
    "theme",
    "GeneratedTheme.kt",
)

writeFileSync(swiftPath, swiftSource())
writeFileSync(kotlinPath, kotlinSource())
console.log(`Generated native theme constants (accent ${theme.accentLight} / ${theme.accentDark}).`)
